package day05_ArithmeticOperators;

public class DivideTwoNumbers {
    public static void main(String[] args) {

        int a = 50;
        int b= 9;
        int division = a/b;
        int reminder = a%b;

        System.out.println(a+" divided by "+b+" is equal to "+ division+
                " with a remainder of "+reminder);




    }
}
