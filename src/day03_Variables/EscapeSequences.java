package day03_Variables;
/*
\n: new line
\t: paragraph space ( tab)
\": double code
\\: double slash
 */

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Hello everyone \nHow are you?");

        System.out.println("Hello everyone");
        System.out.println("My name is Danka");
        System.out.println("I live in Virginia");

        System.out.println("______________");

        System.out.println("\tToday is  great day to study Java");

        System.out.println("______________");

        System.out.println("My favorite tv series is \"Game of Throne\"");



    }
}
