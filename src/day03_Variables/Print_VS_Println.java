package day03_Variables;

public class Print_VS_Println {

    public static void main(String[] args) {


        System.out.println("Hello Batch 23");//appends new line after printing
        System.out.println("How are you today?");

        System.out.println("________________________");


        System.out.print("Hello Batch 23");//it does not append new line
        System.out.print(" How are you today?");//uses just one line-benefit

        System.out.println("________________");
    }
}
