package day21_LoopsAgain;

public class WhileLoopTwo {
    public static void main(String[] args) {

        for (int i = 0; i < 3; i++) {
            System.out.println("Hello batch 23");

        }

        System.out.println("________________________");

        int i = 0;       //if you know how many times you have to repeat it, better to use for loop for this
        while(i<3){
            System.out.println("Hello batch 23");
            i++;
        }











    }
}
