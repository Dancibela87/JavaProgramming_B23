package day21_LoopsAgain;

public class WhileLoopPracticeTwo {
    public static void main(String[] args) {

        int i = 1;

         while(i <= 10) {

             if (i % 2 == 0) {
                 System.out.print(i + " ");

             }
             i++;
         }

        System.out.println();

        System.out.println("_____________________________________________");

        for (int j = 1; j <= 10; j++) {
            System.out.print(j+" ");

        }









    }
}
