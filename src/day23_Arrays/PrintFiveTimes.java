public class PrintFiveTimes {
    public static void main(String[] args) {

        for (int i = 0; i <=5 ; i++) {//if
            System.out.print(i+" ");
        }
        System.out.println();

        System.out.println("____________________");

        for (int j = 6; j <=10 ; j++) {        //how many times

            for (int i = 0; i <=5 ; i++) {     //how many characters
                System.out.print(i+" ");
            }
            System.out.println();              //same as repeating the previous one five times

        }










    }
}
